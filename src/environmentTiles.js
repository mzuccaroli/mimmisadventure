export const ENV_TILE_SPRITES = Object.freeze(
  Object.fromEntries(
    Object.entries(ENV_TILE_ORDER).map(([tileName, tileIndex]) => [
      tileName,
      {
        sprite: spriteId(tileName),
        path: tilePath(tileIndex),
      },
    ]),
  ),
);

export function loadEnvironmentTileAssets(k) {
  Object.values(ENV_TILE_SPRITES).forEach(({ sprite, path }) => {
    k.loadSprite(sprite, path);
  });
}

export function getEnvironmentTileSprite(tileName) {
  const tile = ENV_TILE_SPRITES[tileName];
  if (!tile) {
    throw new Error(`Environment tile "${tileName}" non definito.`);
  }
  return tile.sprite;
}
